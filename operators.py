a=5
b=4

# Arithmetic Operators
print(a+b) # addition
print(a-b) # subtraction
print(a*b) # multiplication
print(a/b) # division with float value
print(a//b) # floor division with integer value
print(a%b) # modulus or remainder
print(a**b) # exponentiation or power

#relational operators
print(a==b) # equal to
print(a!=b) # not equal to
print(a>b) # greater than
print(a<b) # less than
print(a>=b) # greater than or equal to
print(a<=b) # less than or equal to

#assignment operators
c=10
c+=5 # 10+5=15
print(c) # 15

e=10
e-=5 # 10-5=5
print(e) # 5

f=10
f*=5 # f=f*5
print(f) # 50 10*5=50

g=10
g/=5 # g=g/5
print(g) # 2.0 10/5=2.0

h=10
h//=5 # h=h//5
print(h) # 2 10//5=2

i=10
i%=5 # i=i%5
print(i) # 0 10%5=0

j=10
j**=5 # j=j**5
print(j) # 100000

#logical operators
print(not True) # not operator
num1=10
num2=20
print(not(num1>num2)) # not operator with condition

val1=True
val2=False
print("And operator: ",val1 and val2) # and operator with boolean values
print("Or operator: ",val1 or val2) # or operator with boolean values

print(num1< num2 and num1==num2) # and operator

print(num1< num2 or num1==num2) # or operator
